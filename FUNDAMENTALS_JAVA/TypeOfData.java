package FUNDAMENTALS_JAVA;

public class TypeOfData {
  //Values (int, double, float, long)
  //Text (String)
  //Bool (boolean)

  public static void main(String[] args) {
    
    int valueInt = 50;
    double valueDouble = 10.5;
    float valueFloat = 10.5f;
    long valueLong = 10L;
    String text = "Hello, World!";
    boolean valueBoolean = true;

    System.out.println(valueInt);
    System.out.println(valueDouble);
    System.out.println(valueFloat);
    System.out.println(valueLong);
    System.out.println(text);
    System.out.println(valueBoolean);
  }
}
